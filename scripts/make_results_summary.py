"""Generate a single canonical markdown summary of the DML results.

Reads:
  - data/dml_results/dml_results_long_<variant>.parquet  (Stage D)
  - data/main/full_experiment_data_<variant>.parquet     (Stage C, for n's
                                                          and precision breakdown)
  - data/order_probe/order_probe_summary.parquet         (optional, A')

Writes a single markdown:
  - RESULTS_SUMMARY.md  (in the dataset root)

Designed to be runnable both on cluster (after the bf16 redo) and locally
(after a sync-back). Output is deterministic given the same input parquets
so consumers can diff successive runs.

Usage:
    python scripts/make_results_summary.py --data-root <dataset_root>
    python scripts/make_results_summary.py --data-root ~/Hamburg/geodml-dataset \
        --variants biased neutral biased_rag neutral_rag \
        --output ~/Hamburg/geodml-dataset/RESULTS_SUMMARY.md
"""

from __future__ import annotations

import argparse
import datetime as _dt
import os
import sys
from pathlib import Path

import pandas as pd


HEADLINE_SLICE = dict(
    subset="POOLED", method="plr", learner="lgbm", outcome="rank_delta",
)

# Treatment ordering in the headline table (mirrors presentation-rag-2026-05-11.md)
TREATMENT_ORDER = [
    "T1a_stats_present",
    "T1b_stats_density",
    "T2a_question_headings",
    "T2b_structural_modularity",
    "T3_structured_data_new",
    "T4a_ext_citations",
    "T4b_auth_citations",
    "T5_topical_comp",
    "T6_freshness",
    "T7_source_earned",
]


def fmt_coef(coef: float, p: float | None) -> str:
    """+0.123*** style. `p` may be None."""
    if pd.isna(coef):
        return "  —   "
    stars = ""
    if p is not None and not pd.isna(p):
        if p < 0.001: stars = "***"
        elif p < 0.01: stars = "**"
        elif p < 0.05: stars = "*"
    sign = "+" if coef >= 0 else ""
    return f"{sign}{coef:.3f}{stars}"


def fmt_p(p: float | None) -> str:
    if p is None or pd.isna(p):
        return "  -  "
    if p < 0.001: return "<0.001"
    if p < 0.01:  return f"{p:.3f}"
    return f"{p:.3f}"


def load_dml_long(root: Path, variant: str) -> pd.DataFrame | None:
    p = root / "data" / "dml_results" / f"dml_results_long_{variant}.parquet"
    if not p.exists():
        return None
    return pd.read_parquet(p)


def load_main(root: Path, variant: str) -> pd.DataFrame | None:
    p = root / "data" / "main" / f"full_experiment_data_{variant}.parquet"
    if not p.exists():
        return None
    return pd.read_parquet(p)


def headline_row(dml: pd.DataFrame, treatment: str) -> dict | None:
    q = dml.query(
        "subset == @HEADLINE_SLICE['subset']"
        " and method == @HEADLINE_SLICE['method']"
        " and learner == @HEADLINE_SLICE['learner']"
        " and outcome == @HEADLINE_SLICE['outcome']"
        " and treatment == @treatment",
        local_dict=dict(HEADLINE_SLICE=HEADLINE_SLICE, treatment=treatment),
    )
    if q.empty:
        return None
    r = q.iloc[0]
    return {
        "coef": r["coef"], "se": r["se"], "p_val": r["p_val"],
        "n_obs": r["n_obs"], "sig_stars": r["sig_stars"],
        "ci_lower": r["ci_lower"], "ci_upper": r["ci_upper"],
    }


def make_summary(
    root: Path,
    variants: list[str],
    output: Path,
    title: str | None = None,
) -> None:
    now = _dt.datetime.now(_dt.timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    title = title or f"DML Results Summary — {now}"

    # ── Load per-variant artifacts ────────────────────────────────────────
    dmls: dict[str, pd.DataFrame] = {}
    mains: dict[str, pd.DataFrame] = {}
    for v in variants:
        d = load_dml_long(root, v)
        m = load_main(root, v)
        if d is not None: dmls[v] = d
        if m is not None: mains[v] = m

    if not dmls:
        print(f"ERROR: no dml_results_long_*.parquet found under {root}/data/dml_results/",
              file=sys.stderr)
        sys.exit(2)

    # ── Build the markdown ────────────────────────────────────────────────
    lines: list[str] = []
    lines.append(f"# {title}")
    lines.append("")
    lines.append(f"Generated by `scripts/make_results_summary.py` from")
    lines.append(f"`data/dml_results/dml_results_long_*.parquet` and")
    lines.append(f"`data/main/full_experiment_data_*.parquet`.")
    lines.append("")
    lines.append(f"- **Data root**: `{root}`")
    lines.append(f"- **Variants**: {', '.join(variants)}")
    lines.append(f"- **Headline slice**: `{HEADLINE_SLICE}`")
    lines.append("")

    # ── Section 1: Per-variant inventory ──────────────────────────────────
    lines.append("## 1. Per-variant inventory")
    lines.append("")
    lines.append("| Variant | Stage C rows | Stage D fits | DML errors | "
                 "Records · LLM backend × precision |")
    lines.append("|---|---:|---:|---:|---|")
    for v in variants:
        dml = dmls.get(v)
        main = mains.get(v)
        if dml is None and main is None:
            lines.append(f"| `{v}` | — | — | — | (no artifacts) |")
            continue
        c_rows = f"{len(main):,}" if main is not None else "—"
        d_fits = f"{len(dml):,}" if dml is not None else "—"
        d_errs = (
            f"{int(dml['error'].notna().sum()):,}"
            if dml is not None and "error" in dml.columns else "—"
        )
        if main is not None and "llm_precision" in main.columns:
            breakdown = (
                main.groupby(["llm_backend", "llm_precision"]).size().to_dict()
            )
            br = "; ".join(f"{b}/{p}: {n:,}" for (b, p), n in breakdown.items())
        else:
            br = "(missing precision column — re-run merge.py)"
        lines.append(f"| `{v}` | {c_rows} | {d_fits} | {d_errs} | {br} |")
    lines.append("")

    # ── Section 2: Headline coefficient table ─────────────────────────────
    lines.append("## 2. Headline coefficients — `POOLED · plr · lgbm · rank_delta`")
    lines.append("")
    header = "| Treatment |"
    sep    = "|---|"
    for v in variants:
        header += f" {v} |"
        sep    += "---:|"
    lines.append(header)
    lines.append(sep)
    for t in TREATMENT_ORDER:
        row = f"| `{t}` |"
        for v in variants:
            dml = dmls.get(v)
            if dml is None:
                row += "   —    |"
                continue
            r = headline_row(dml, t)
            row += f" {fmt_coef(r['coef'], r['p_val']) if r else '   —   '} |"
        lines.append(row)
    lines.append("")
    lines.append("Sig codes: `***` p<0.001 · `**` p<0.01 · `*` p<0.05.")
    lines.append("")

    # ── Section 3: 2x2 grid (prompt × method) for the two interesting Ts ──
    snippet_b, snippet_n = "biased", "neutral"
    rag_b, rag_n = "biased_rag", "neutral_rag"
    if all(v in dmls for v in (snippet_b, snippet_n, rag_b, rag_n)):
        lines.append("## 3. The 2×2 grid (prompt × method)")
        lines.append("")
        lines.append("For the two treatments where snippet↔RAG comparison is most informative:")
        lines.append("")
        for t in ("T7_source_earned", "T5_topical_comp"):
            lines.append(f"### `{t}`")
            lines.append("")
            r_bs = headline_row(dmls[snippet_b], t)
            r_ns = headline_row(dmls[snippet_n], t)
            r_br = headline_row(dmls[rag_b], t)
            r_nr = headline_row(dmls[rag_n], t)

            def cell(r):
                if r is None: return "—"
                return f"{fmt_coef(r['coef'], r['p_val'])}  (n={r['n_obs']:,})"

            lines.append("| | snippet | RAG |")
            lines.append("|---|---|---|")
            lines.append(f"| **biased** | {cell(r_bs)} | {cell(r_br)} |")
            lines.append(f"| **neutral** | {cell(r_ns)} | {cell(r_nr)} |")
            lines.append("")

            if r_bs and r_ns and r_br and r_nr:
                gap_snip = r_bs["coef"] - r_ns["coef"]
                gap_rag  = r_br["coef"] - r_nr["coef"]
                aug_b    = r_br["coef"] - r_bs["coef"]
                aug_n    = r_nr["coef"] - r_ns["coef"]
                inter    = aug_b - aug_n
                lines.append(f"- Snippet prompt gap (biased − neutral): **{gap_snip:+.3f}**")
                lines.append(f"- RAG prompt gap (biased − neutral): **{gap_rag:+.3f}**")
                lines.append(f"- Aug effect within biased (rag − snip): **{aug_b:+.3f}**")
                lines.append(f"- Aug effect within neutral (rag − snip): **{aug_n:+.3f}**")
                lines.append(f"- Interaction: **{inter:+.3f}**")
            lines.append("")

    # ── Section 4: Subset breakdown for T7 (the headline finding) ─────────
    lines.append("## 4. T7 (`source_earned`) — by subset")
    lines.append("")
    for v in variants:
        dml = dmls.get(v)
        if dml is None: continue
        rows = dml.query(
            "treatment == 'T7_source_earned'"
            " and method == @HEADLINE_SLICE['method']"
            " and learner == @HEADLINE_SLICE['learner']"
            " and outcome == @HEADLINE_SLICE['outcome']",
            local_dict=dict(HEADLINE_SLICE=HEADLINE_SLICE),
        )
        if rows.empty: continue
        lines.append(f"### `{v}`")
        lines.append("")
        lines.append("| Subset | coef | p | n | CI |")
        lines.append("|---|---:|---:|---:|---|")
        for _, r in rows.sort_values("subset").iterrows():
            lines.append(
                f"| `{r['subset']}` | {fmt_coef(r['coef'], r['p_val'])} | "
                f"{fmt_p(r['p_val'])} | {int(r['n_obs']):,} | "
                f"[{r['ci_lower']:+.3f}, {r['ci_upper']:+.3f}] |"
            )
        lines.append("")

    # ── Section 5: Precision stratification ───────────────────────────────
    lines.append("## 5. Precision stratification (`llm_precision`)")
    lines.append("")
    lines.append("Counts of source-of-truth records by execution regime per variant:")
    lines.append("")
    lines.append("| Variant | `bf16-full` | `4bit-nf4` | `api-hf` | `api-openai` | `unknown` |")
    lines.append("|---|---:|---:|---:|---:|---:|")
    for v in variants:
        main = mains.get(v)
        if main is None or "llm_precision" not in main.columns:
            lines.append(f"| `{v}` | — | — | — | — | — |")
            continue
        c = main["llm_precision"].value_counts().to_dict()
        lines.append(
            f"| `{v}` | {c.get('bf16-full', 0):,} | {c.get('4bit-nf4', 0):,} | "
            f"{c.get('api-hf', 0):,} | {c.get('api-openai', 0):,} | "
            f"{c.get('unknown', 0):,} |"
        )
    lines.append("")
    lines.append("Use the `llm_precision` column to stratify when comparing snippet vs RAG —")
    lines.append("see PROVENANCE.md § '2026-05-17 update' for the taxonomy.")
    lines.append("")

    # ── Section 6: Cross-pair comparison files (if present) ───────────────
    comp_dir = root / "data" / "dml_results"
    extras = [
        ("rag_vs_snippet_comparison.csv", "Paired snippet↔RAG deltas per treatment"),
        ("rag_vs_snippet_4way.csv", "Full 2×2 with interaction term"),
        ("dml_results_long_ALL.csv", "Concatenated long table for all variants"),
    ]
    present = [(f, d) for f, d in extras if (comp_dir / f).exists()]
    if present:
        lines.append("## 6. Companion analysis files")
        lines.append("")
        for f, d in present:
            sz = (comp_dir / f).stat().st_size
            lines.append(f"- `data/dml_results/{f}` ({sz:,} bytes) — {d}")
        lines.append("")

    # ── Section 7: Reproducibility ────────────────────────────────────────
    lines.append("## 7. Reproducibility")
    lines.append("")
    lines.append("```bash")
    lines.append(f"# Regenerate this summary:")
    lines.append(f"python scripts/make_results_summary.py \\")
    lines.append(f"    --data-root {root} \\")
    lines.append(f"    --variants {' '.join(variants)} \\")
    lines.append(f"    --output {output}")
    lines.append("```")
    lines.append("")
    lines.append("Pipeline scripts (in `~/Hamburg/GEODML_Analysis`):")
    lines.append("")
    lines.append("- Stage A: `interpretability/pipeline/rerank.py`")
    lines.append("- Stage A': `interpretability/pipeline/order_probe.py`")
    lines.append("- Stage B: `interpretability/pipeline/features.py`")
    lines.append("- Stage C: `interpretability/pipeline/merge.py`")
    lines.append("- Stage D: `interpretability/pipeline/dml.py`")
    lines.append("- Cluster driver: `scripts/finish_on_gpu.sh`")
    lines.append("- Local driver: `scripts/continue_pipeline.sh`")
    lines.append("- Backfill precision: `scripts/backfill_precision.py`")
    lines.append("")

    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text("\n".join(lines))
    print(f"Wrote summary → {output}")
    print(f"  variants:  {len(variants)}")
    print(f"  dml found: {sorted(dmls)}")
    print(f"  main found: {sorted(mains)}")


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--data-root", required=True,
                    help="Dataset root (parent of data/dml_results/, data/main/).")
    ap.add_argument("--variants", nargs="+",
                    default=["biased", "neutral", "biased_rag", "neutral_rag"],
                    help="Variants to include (default: 4-variant cross).")
    ap.add_argument("--output", default=None,
                    help="Output path (default: <data-root>/RESULTS_SUMMARY.md)")
    ap.add_argument("--title", default=None,
                    help="Optional custom H1 for the summary.")
    args = ap.parse_args()

    root = Path(args.data_root).expanduser().resolve()
    output = Path(args.output).expanduser() if args.output else root / "RESULTS_SUMMARY.md"

    if not (root / "data" / "dml_results").is_dir():
        print(f"ERROR: {root}/data/dml_results not found", file=sys.stderr)
        return 2

    make_summary(root, args.variants, output, title=args.title)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
